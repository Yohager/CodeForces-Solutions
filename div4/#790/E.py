import bisect 

def calc(m,n,nums,q):
    nums.sort(reverse=True)
    f = [0] * m
    f[0] = nums[0]
    for i in range(1,m):
        f[i] = f[i-1] + nums[i]
    # f is the prefix 
    res = []
    for qv in q:
        idx = bisect.bisect_left(f,qv)
        if idx == m:
            res.append(-1)
        else:
            res.append(idx+1)
    return res 

